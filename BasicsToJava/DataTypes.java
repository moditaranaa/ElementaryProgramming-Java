package BasicsToJava;

public class DataTypes {
    /* Data types in Java can be classified into 2 types :
    1. Primitive :
        (i). Boolean Type -
                1. boolean values - true/false.
        (ii). Numeric Type-
                1. Character values - char
                2. Integral values-
                     i. Integer -
                            byte
                            short
                            int
                            long
                    ii. Floating Point-
                            double
                            float
    2. Non-Primitive-
         (i). String
         (ii). Array
    */
    public static void main(String[] args){
        int a= 3;
        double d = 3.34;
        char c = 'A';
        String s= "I am a String";
        boolean b = true;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(s);
    }
}
